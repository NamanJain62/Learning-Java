public class resultingdatatype {
    public static void main(String[] args) {
        int a=46+8;
        short x=8;
        int z=x+a;
        System.out.println(z);

        //Increment and decrement
        int i =45;
        System.out.println(i++);//firstly we assign i (45) then i increment(46)
        System.out.println(i);
        System.out.println(++i);//firstly increment i (47) then i assign (47)
        System.out.println(i);

    }
}
